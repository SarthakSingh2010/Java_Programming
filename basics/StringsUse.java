public class StringsUse { 
	public static void main(String[] args) {  
		String t="Hello"; //Create a Sting 
		int a=10; //Integer  variable
		String blank=" "; // blank String
		String name="Sarthak";
		
		String greet=t+blank+name; //String Concatenation
		System.out.println(greet); //Print String
		
		System.out.println("Hello"+" "+"this is Java.");
		System.out.println("My Integer :: "+a);// Print String along with Integer
		
		double b=1234.566789;
		System.out.println("My Double :: "+b);	// Print String with Double
	} 
} 
